package PractiseProblems;

public class PG2 {
    public static void main(String args[]){
        int a=10,b=10;
        System.out.println("PostIncrement:"+(a++));//value then increment
        System.out.println("PreIncrement:"+(++a));//increment the value
        System.out.println("PostDecrement:"+(b--));//value the decrement
        System.out.println("PreDecrement:"+(--b));//decrement then value
    }
}

